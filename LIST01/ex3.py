tv=['lg','samsung','videocon','sony','micromax','ekkeda']
print(tv)
# ['lg', 'samsung', 'videocon', 'sony', 'micromax', 'ekkeda']

# list extend method it has taken more than one argument..,it added the end of the list
tv.extend(['macro','sonika','flattern'])
print(tv)
# ['lg', 'samsung', 'videocon', 'sony', 'micromax', 'ekkeda', 'macro', 'sonika', 'flattern']
tv.extend(['airtel','idea'])
print(tv)
# ['lg', 'samsung', 'videocon', 'sony', 'micromax', 'ekkeda', 'macro', 'sonika', 'flattern', 'airtel', 'idea']
